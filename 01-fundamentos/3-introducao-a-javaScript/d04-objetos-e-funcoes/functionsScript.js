
function sum(a, b) {
    return a + b;
}
function subtraction(a, b) {
    return a - b;
}
function division(a, b) {
    return a / b;
}
function multiplication(a, b) {
    return a * b;
}
function mod(a, b) {
    return a % b;
}
console.log(sum(100, 50));
console.log(subtraction(100, 50));
console.log(division(100, 50));
console.log(multiplication(100, 50));
console.log(mod(100, 50));